#arrithmetic opperators
p=float(input("Enter a 1st number: "))
q=float(input("Enter a 2nd number: "))

sum=p+q  #sum
print(sum)

diff=p-q    #subtract
print(diff)

prod=p*q     #product
print(prod)

div=p/q      #divison
print(div)

power=p**q    #power(**)
print(power)

mod=p%q       #modulue(%)-remainder
print(mod)

floordiv=p//q   #floor division(//)-nearest int
print(floordiv)

